#Define a lambda function to calculate the power of a number.
#
#parameters:
# base (int): The base number to be raised to a power
# power (int): The exponent idication the power to which the base is raised.
# Returns:
# The result of raising 'base' to the power of 'exp'

power_to = lambda base, exp: base ** exp

print(power_to(2, 10))
